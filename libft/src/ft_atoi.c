#include "../inc/libft.h"

int	ft_atoi(const char *str)
{
	long	nb;
	long	sign;

	nb = 0;
	sign = 1;
	while (ft_strchr("\f\n\r\v\t ", *str) && *str)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		nb = nb * 10 + *str - '0';
		str++;
	}
	return (nb * sign);
}
