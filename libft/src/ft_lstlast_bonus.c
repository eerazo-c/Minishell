#include "../inc/libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*ref;

	if (!lst)
		return (NULL);
	ref = lst;
	while (ref->next != NULL)
		ref = ref->next;
	return (ref);
}
