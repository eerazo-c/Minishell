#include "../inc/libft.h"

int	ft_isspace(int c)
{
	if (c != '\f' && c != '\n')
	{
		if (c != '\v' && c != '\r')
		{
			if (c != '\t' && c != ' ')
				return (0);
		}
	}
	return (1);
}
