#include "../include/minishell.h"

int	main(int argc, char **argv, char **envp)
{
	t_shell	shell;

	if (argc != 1 || *envp == NULL)
		return (1);
	(void)argv;
	init_shell(&shell, envp);
	shell_loop(&shell);
	wrapper_free_lst(shell.raw_env);
	free_array(shell.env);
	return (shell.last_status);
}
